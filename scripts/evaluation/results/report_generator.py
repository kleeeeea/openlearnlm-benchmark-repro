"""Report generator — reads JSONL response files and produces JSON + Markdown reports."""

import json
from datetime import datetime
from pathlib import Path
from typing import Dict, Any, List

from evaluation.engine.answer_checker import SCORE_FIELD
from evaluation.engine.evaluator import CHECK_RESULT_FIELD


class ReportGenerator:
    def __init__(self, responses_dir: Path, reports_dir: Path):
        self.responses_dir = responses_dir
        self.reports_dir = reports_dir
        self.reports_dir.mkdir(parents=True, exist_ok=True)

    def _load_results(self) -> Dict[str, List[Dict[str, Any]]]:
        results: Dict[str, List[Dict]] = {}
        if not self.responses_dir.exists():
            return results
        for path in sorted(self.responses_dir.glob("*.jsonl")):
            items = []
            with open(path, "r", encoding="utf-8") as f:
                for line in f:
                    line = line.strip()
                    if line:
                        try:
                            items.append(json.loads(line))
                        except json.JSONDecodeError:
                            pass
            if items:
                model_id = items[0].get("model", path.stem)
                results[model_id] = items
        return results

    def _compute_stats(self, items: List[Dict[str, Any]]) -> Dict[str, Any]:
        successful = [r for r in items if r.get("success", False)]
        correct = [r for r in successful if r.get("is_correct", False)]
        total_latency = sum(r.get("latency_ms", 0) for r in successful)
        total_prompt = sum((r.get("usage") or {}).get("prompt_tokens", 0) for r in successful)
        total_completion = sum((r.get("usage") or {}).get("completion_tokens", 0) for r in successful)

        scores = []
        for item in items:
            check_result = item.get(CHECK_RESULT_FIELD) or {}
            score = check_result.get(SCORE_FIELD)
            if score is None:
                score = 10.0 if item.get("is_correct", False) else 0.0
            scores.append(float(score))

        return {
            "total": len(items),
            "score": sum(scores) / len(scores) if scores else 0.0,
            "successful": len(successful),
            "failed": len(items) - len(successful),
            "correct": len(correct),
            "accuracy": round(len(correct) / len(successful), 4) if successful else 0.0,
            "avg_latency_ms": round(total_latency / len(successful), 1) if successful else 0.0,
            "total_prompt_tokens": total_prompt,
            "total_completion_tokens": total_completion,
        }

    def generate_report(self) -> Dict[str, Any]:
        results = self._load_results()
        if not results:
            return {"error": f"No response files found in {self.responses_dir}"}

        timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
        report: Dict[str, Any] = {
            "generated_at": timestamp,
            "responses_dir": str(self.responses_dir),
            "models": {model_id: self._compute_stats(items) for model_id, items in results.items()},
        }

        ts = datetime.now().strftime("%Y%m%d_%H%M%S_%f")
        json_path = self.reports_dir / f"report_{ts}.json"
        with open(json_path, "w", encoding="utf-8") as f:
            json.dump(report, f, indent=2, ensure_ascii=False)

        md_path = self.reports_dir / f"report_{ts}.md"
        with open(md_path, "w", encoding="utf-8") as f:
            f.write(self._render_markdown(report))

        return {
            "json_report": str(json_path),
            "markdown_report": str(md_path),
            "summary": report,
        }

    def _render_markdown(self, report: Dict[str, Any]) -> str:
        lines = [
            "# OpenLearnLM Benchmark Report",
            "",
            f"Generated: {report['generated_at']}",
            f"Source: `{report['responses_dir']}`",
            "",
            "## Results by Model",
            "",
            "| Model | Total | Correct | Accuracy | Avg Latency | Prompt Tokens | Completion Tokens |",
            "|-------|-------|---------|----------|-------------|---------------|-------------------|",
        ]
        for model_id, s in report["models"].items():
            lines.append(
                f"| {model_id} | {s['total']} | {s['correct']} | {s['accuracy']*100:.1f}% "
                f"| {s['avg_latency_ms']:.0f}ms | {s['total_prompt_tokens']:,} | {s['total_completion_tokens']:,} |"
            )
        lines += ["", "---", "*Report generated by OpenLearnLM Benchmark*", ""]
        return "\n".join(lines)
